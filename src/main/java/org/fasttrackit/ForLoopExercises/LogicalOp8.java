package org.fasttrackit.ForLoopExercises;

public class LogicalOp8 {
    int b;
    int c;
    int d;

    public void op8 (int b) {

        for (int i = b; i < (100+i); i++) {
            if (b < 100) {
                c = c + i;
                b++;
                d++;
            }
        }
        System.out.println(c/d);
    }
}
